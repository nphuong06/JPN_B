// Sử dụng hàm Math.abs()
let num1 = -5.5;
let result1 = Math.abs(num1);
console.log(result1); // Kết quả: 5.5 vì Math.abs() trả về giá trị tuyệt đối

let num2 = 10;
let result2 = Math.abs(num2);
console.log(result2); // Kết quả: 10 vì 10 đã là số dương

// Sử dụng hàm Math.ceil()
let num3 = 4.4;
let result3 = Math.ceil(num3);
console.log(result3); // Kết quả: 5 vì Math.ceil() làm tròn lên số nguyên gần nhất

let num4 = 5.8;
let result4 = Math.ceil(num4);
console.log(result4); // Kết quả: 6 vì làm tròn lên số nguyên gần nhất

// Sử dụng hàm Math.floor()
let num5 = 5.7;
let result5 = Math.floor(num5);
console.log(result5); // Kết quả: 5 vì Math.floor() làm tròn xuống số nguyên gần nhất

let num6 = 6.1;
let result6 = Math.floor(num6);
console.log(result6); // Kết quả: 6 vì làm tròn xuống số nguyên gần nhất

// Sử dụng hàm Math.round()
let num7 = 10.7;
let result7 = Math.round(num7);
console.log(result7); // Kết quả: 11 vì Math.round() làm tròn theo quy tắc toán học (>= 0.5 thì làm tròn lên)

let num8 = 20.6;
let result8 = Math.round(num8);
console.log(result8); // Kết quả: 21 vì >= 0.5 thì làm tròn lên
